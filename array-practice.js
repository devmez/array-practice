
let deck = [];
let suits = "hsdc";
let numbers = "123456789tjqk";
let userPrompt = require(`readline-sync`);

function initDeck(){
    //returns a deck 52 cards without joker
    for (let i = 0; i < 4; i ++){
        for (let a = 0; a < 13; a ++){
            deck.push(suits[i] + numbers[a]);
        };
    };

    return deck;
}
initDeck();

function shuffleDeck(newDeck){
    //shuffles deck
    let numPos = null;
    let num2 = null;
    
    for (let a = deck.length - 1; a > 0; a--){
        numPos = Math.floor(Math.random() * (a + 1)); //random array-position generator
        num2 = deck[a]; //num2 = first card generated by for-loop
        deck[a] = deck[numPos]; //we want to switch 'num2' for a card randomly picked.
        deck[numPos] = num2; // ...then replace the randomly picked card with our 'num2' card.
    };

    return newDeck;
};
shuffleDeck(deck);

